class usuario: 
    def __init__(self, user, pwd, name, curp, city, rol = "cliente") -> None:
        self.user = user
        self.pwd = pwd
        self.name = name
        self.curp = curp
        self.city = city
        self.rol = rol

@property
def user(self):
    return self.user

def pwd(self):
    return self.pwd

def rol(self):
    return self.rol

def name(self):
    return self.name

def curp(self):
    return self.curp

def city(self):
    return self.city

def curp(self):
    return self.curp
